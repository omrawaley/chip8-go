package chip8

import "fmt"

const memorySize = 4096

var fontset = [80]byte{
	0xF0, 0x90, 0x90, 0x90, 0xF0, // 0
	0x20, 0x60, 0x20, 0x20, 0x70, // 1
	0xF0, 0x10, 0xF0, 0x80, 0xF0, // 2
	0xF0, 0x10, 0xF0, 0x10, 0xF0, // 3
	0x90, 0x90, 0xF0, 0x10, 0x10, // 4
	0xF0, 0x80, 0xF0, 0x10, 0xF0, // 5
	0xF0, 0x80, 0xF0, 0x90, 0xF0, // 6
	0xF0, 0x10, 0x20, 0x40, 0x40, // 7
	0xF0, 0x90, 0xF0, 0x90, 0xF0, // 8
	0xF0, 0x90, 0xF0, 0x10, 0xF0, // 9
	0xF0, 0x90, 0xF0, 0x90, 0x90, // A
	0xE0, 0x90, 0xE0, 0x90, 0xE0, // B
	0xF0, 0x80, 0x80, 0x80, 0xF0, // C
	0xE0, 0x90, 0x90, 0x90, 0xE0, // D
	0xF0, 0x80, 0xF0, 0x80, 0xF0, // E
	0xF0, 0x80, 0xF0, 0x80, 0x80, // F
}

type Memory struct {
	bus [memorySize]byte
}

func NewMemory() *Memory {
	m := &Memory{}
	m.loadFontset()
	return m
}

func (m *Memory) loadFontset() {
	for addr, val := range fontset {
		m.Write(uint16(addr), val)
	}
}

func (m *Memory) Read(addr uint16) (byte, error) {
	if addr >= memorySize {
		return 0, fmt.Errorf("out of bounds read address 0x%04X", addr)
	}

	return m.bus[addr], nil
}

func (m *Memory) Write(addr uint16, val byte) error {
	if addr >= memorySize {
		return fmt.Errorf("out of bounds write address 0x%04X", addr)
	}

	m.bus[addr] = val
	return nil
}
